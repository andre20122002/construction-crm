def parse_bool(value, default=False):
    """Helper to parse boolean values from environment variables."""
    if value is None:
        return default
    # .strip() видаляє випадкові пробіли, які можуть бути в .env
    return str(value).strip().lower() in ('true', '1', 't', 'y', 'yes')

def parse_csv(value):
    """Helper to parse comma-separated strings into a list."""
    if not value:
        return []
    return [x.strip() for x in value.split(',') if x.strip()]


